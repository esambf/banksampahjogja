    @Override
    public boolean onOptionsItemSelected(MenuItem item) {
        if (item.getItemId() == R.id.logout) {
            startActivity(new Intent(getActivity(), Login.class));
            Toast.makeText(getActivity(), "Logout Berhasil!", Toast.LENGTH_SHORT).show();
        }
        return super.onOptionsItemSelected(item);
    }

    @Override
    public void onViewCreated(@NonNull View view, Bundle savedInstanceState) {
        Button Submit = view.findViewById(R.id.btnSubmita);
        final Spinner spinner1, spinner2, spinner3, spinner4;
        spinner1 = view.findViewById(R.id.spinner1);
        spinner2 = view.findViewById(R.id.spinner2);
        spinner3 = view.findViewById(R.id.spinner3);
        spinner4 = view.findViewById(R.id.spinner4);

        Submit.setOnClickListener(new View.OnClickListener() {
            @Override
            public void onClick(View view) {
                Intent i = new Intent(getActivity().getApplicationContext(), Rekap.class);
                i.putExtra("bank",spinner1.getSelectedItem().toString());
                i.putExtra("hargap",spinner2.getSelectedItem().toString());
                i.putExtra("hargab",spinner3.getSelectedItem().toString());
                i.putExtra("periode",spinner4.getSelectedItem().toString());
                startActivity(i);
            }
        });
    }







